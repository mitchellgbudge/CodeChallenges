/*
 
 Create a function that takes a number as an argument, increments the number by +1 and returns the result.
 
 Examples
 addition(0) ➞ 1
 addition(9) ➞ 10
 addition(-3) ➞ -2
 
 */

func simpleAddition(number: Int) -> Int {
    let sum = number + 1
    return sum
}

simpleAddition(number: 0)
simpleAddition(number: 9)
simpleAddition(number: -3)
